#Create a variable called 'lives' to keep track of the number of lives left. 
#Set 'lives' to equal 6.
lives = len(stages) - 1

#Create an empty List called display.
#For each letter in the chosen_word, add a "_" to 'display'.
#So if the chosen_word was "apple", display should be ["_", "_", "_", "_", "_"] with 5 "_" representing each letter to guess.
display = []
previous_guesses = []
for letter in chosen_word:
    display.append("_")

#Greet user
print(logo)
print(stages[lives])
print(display)

#Ask the user to guess a letter and assign their answer to a variable called guess. Make guess lowercase.
#Use a while loop to let the user guess again. The loop should only stop once the user has guessed all the letters in the chosen_word and 'display' has no more blanks ("_"). Then you can tell the user they've won.
while not game_over:
    guess = input("Guess a letter:\n").strip().lower()

    #Verify guess is valid and has not been entered previously
    while guess not in valid_guesses or guess in previous_guesses:
        if guess not in valid_guesses:
            guess = input("'" + guess + "' is not a letter. Guess a letter:\n").strip().lower()
        elif guess in previous_guesses:
            guess = input("You have already guessed '" + guess + "'. Guess another letter:\n").strip().lower()
        else:
            print("An unexpected error has caused the game to terminate.")
            exit()

    #Add guess to previous_guesses
    previous_guesses.append(guess)
    system("cls") # clear console

    #If guess is not a letter in the chosen_word,
    #Then reduce 'lives' by 1. 
    #If lives goes down to 0 then the game should stop and it should print "You lose."
    if guess not in chosen_word:
        lives -= 1
        print("'" + guess + "' is not in the word.")
    if lives < 1:
        print(stages[0])
        print("You lose.")
        print("The word was:", chosen_word)
        game_over = True
        continue

    #Check if the letter the user guessed (guess) is one of the letters in the chosen_word.
    #Loop through each position in the chosen_word;
    #If the letter at that position matches 'guess' then reveal that letter in the display at that position.
    #e.g. If the user guessed "p" and the chosen word was "apple", then display should be ["_", "p", "p", "_", "_"].
    for letter in range(len(chosen_word)):
        if guess == chosen_word[letter]:
            display[letter] = guess

    if "_" not in display:
        print(stages[lives])
        print(display)
        print("You win.")
        print("The word was:", chosen_word)
        game_over = True
        continue

    #Print the ASCII art from 'stages' that corresponds to the current number of 'lives' the user has remaining.
    print(stages[lives])
    #Print 'display' and you should see the guessed letter in the correct position and every other letter replaced with "_".
    print(display)

